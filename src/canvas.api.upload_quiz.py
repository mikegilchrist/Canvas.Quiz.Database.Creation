# ============================================================================
# Script:       canvas.api.upload_quiz.py
# Version:      1.0.0
# Date:         2026-03-01
# Purpose:      Upload a QTI zip to Canvas as a Classic Quiz, position it
#               by session order among published quizzes, and optionally
#               publish it.  Offers to delete any existing quiz with the
#               same session prefix (e.g., an old S12 from 2025).
#
# Workflow:
#   1. Read quiz title from the QTI zip (assessment_meta.xml)
#   2. Snapshot existing quiz IDs
#   3. Upload zip via Content Migrations API and poll until complete
#   4. Identify the new quiz by diffing quiz lists
#   5. Check for existing quizzes with the same session prefix; offer to delete
#   6. Position the new quiz after the last published quiz of lower/equal
#      session number (not at the bottom as Canvas does by default)
#   7. Optionally publish
#
# Usage:        canvas.api.upload_quiz.py COURSE_ID QTI_ZIP
#                   [--publish] [--yes]
#                   [--base-url URL] [--token TOKEN | --token-file FILE]
#                   [--poll-interval SEC] [--dry-run] [--verbose] [--help]
#
# Input:        A QTI zip file (e.g., from text2qti) and a Canvas course ID
# Output:       Upload/positioning status and quiz URL on stdout
#
# Options:
#   --publish              Publish the quiz after upload
#   --yes                  Auto-confirm deletion of old same-prefix quiz
#   --base-url URL         Canvas instance URL (or set in ~/.canvas.api.conf)
#   --token TOKEN          Inline API token
#   --token-file FILE      Path to file containing API token
#   --poll-interval SEC    Seconds between migration status polls (default: 5)
#   -n, --dry-run          Show what would be done without uploading
#   -v, --verbose          Show detailed output
#   -h, --help             Show this help message
#
# Requirements:
#   - Python 3.7+
#   - Canvas API token with 'Manage course content' permission
#   - ~/.canvas.api.conf with base_url and token_file (or pass via CLI)
#
