# ============================================================================
# Script:       canvas.api.update_lecture_page.py
# Version:      1.0.0
# Date:         2026-03-01
# Purpose:      Update the Quiz and/or Slides section of a Canvas session
#               page (e.g. S12) with links to the published quiz and/or
#               uploaded slides PDF.
#
#               Session pages follow this structure:
#                 <h3>Reading[s]</h3>  -- not touched
#                 <h3>Quiz</h3>        -- updated by --quiz-id
#                 <h3>Slides</h3>      -- updated by --slides or --slides-id
#
#               Links are generated in Canvas's native HTML format so they
#               render correctly (quiz link with data-api-* attrs; file link
#               with verifier token and instructure_file_link class).
#
# Usage:        canvas.api.update_lecture_page.py COURSE_ID SESSION
#                   [--quiz-id QUIZ_ID]
#                   [--slides FILENAME | --slides-id FILE_ID]
#                   [--base-url URL] [--token TOKEN | --token-file FILE]
#                   [--dry-run] [--verbose] [--help]
#
# Input:        Canvas course ID; session prefix (e.g. S12 or S06a)
# Output:       Updated page URL and change summary on stdout
#
# Options:
#   SESSION              Session prefix, e.g. S12 or S06a
#   --quiz-id QUIZ_ID    Canvas quiz ID to link in the Quiz section
#   --slides FILENAME    Search Canvas files by filename to link in Slides
#   --slides-id FILE_ID  Use known Canvas file ID for slides link
#   --base-url URL       Canvas instance URL (or set in ~/.canvas.api.conf)
#   --token TOKEN        Inline API token
#   --token-file FILE    Path to file containing API token
#   -n, --dry-run        Show current and proposed section content without
#                        making changes
#   -v, --verbose        Show detailed output
#   -h, --help           Show this help message
#
# Requirements:
#   - Python 3.7+
#   - Canvas API token with page edit permissions
#   - ~/.canvas.api.conf with base_url and token_file (or pass via CLI)
#
